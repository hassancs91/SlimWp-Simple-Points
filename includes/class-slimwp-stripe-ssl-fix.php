class SlimWP_Stripe_SSL_Fix {
    
    public static function ensure_ca_bundle() {
        $ca_bundle_path = SLIMWP_PLUGIN_DIR . 'includes/stripe-php/data/ca-certificates.crt';
        
        // If CA bundle doesn't exist, create it
        if (!file_exists($ca_bundle_path)) {
            self::create_ca_bundle($ca_bundle_path);
        }
        
        return $ca_bundle_path;
    }
    
    private static function create_ca_bundle($path) {
        // Create directory if it doesn't exist
        $dir = dirname($path);
        if (!is_dir($dir)) {
            wp_mkdir_p($dir);
        }
        
        // Try to get CA bundle from WordPress or system
        $ca_content = '';
        
        // Option 1: Use WordPress built-in certificates
        if (function_exists('wp_remote_get')) {
            // Try to use the system's CA bundle
            $curl_info = curl_version();
            if (!empty($curl_info['cainfo']) && file_exists($curl_info['cainfo'])) {
                $ca_content = file_get_contents($curl_info['cainfo']);
            }
        }
        
        // Option 2: Use a minimal CA bundle for Stripe
        if (empty($ca_content)) {
            $ca_content = self::get_minimal_ca_bundle();
        }
        
        // Write the CA bundle
        if (!empty($ca_content)) {
            file_put_contents($path, $ca_content);
            error_log('SlimWP Stripe: Created CA bundle at ' . $path);
            return true;
        }
        
        return false;
    }
    
